list = [2,1,3]
list.append(4)  # add element at the end 
print(list)

list.sort()    # sort in ascending order
print(list)

list.sort(reverse=True)    # sort in descending order
print(list)

list.reverse()      # reverse list
print(list)

list.insert(2,8)    #insert element at index 
print(list)

list.remove(1)    # remove first ocurrance of element  
print(list)

list.pop(2)    # remove element at index
print(list)